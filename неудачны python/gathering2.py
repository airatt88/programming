# Размер таблицы
N, M = 12, 12

# Создаем таблицу значений F(n, m)
F = [[0] * (M + 1) for _ in range(N + 1)]

# Заполняем известные случаи
for n in range(1, N + 1):
    F[n][1] = 0  # Если 1 мяч, процесс завершен
for m in range(1, M + 1):
    F[1][m] = 0  # Если 1 чаша, процесс завершен

# Основной расчет F(n, m)
for n in range(2, N + 1):
    for m in range(2, M + 1):
        F[n][m] = 1 + (m - 1) / m * F[n][m - 1] + 1 / m * F[n - 1][m]

# Вычисляем G(12, 12)
G = sum(F[n][m] for n in range(2, N + 1) for m in range(2, M + 1))

# Вывод результата в научном формате с 12 знаками после запятой
print(f"G(12, 12) = {G:.12e}")
